using UnityEngine;

public class SlashAttack : MonoBehaviour
{
    public float lifetime = 0.2F;

    private void Start()
    {
        Destroy(gameObject, lifetime);
    }

    private void OnTriggerEnter2D(Collider2D other)
    {
        if (other.CompareTag("Enemy"))
        {
            Destroy(other.gameObject);
        }
    }
}